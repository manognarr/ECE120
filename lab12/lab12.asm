				BRz  DONER       ; check if column is zero
				ADD R1, R1, #0 ; 
				BRn  PRINTONE
				LDI R0, CHARFORZERO
				OUT

SHIFT 				ADD R1, R1, R1
				ADD R4, R4, #-1
				BRnzp COLUMNN
 
DONER				 LD R0, NEW_LINE
	  		 	 OUT
			 	ADD R3, R3, #-1 
				ADD R2, R2, #1
				BRnzp CHECKR ;

PRINTONE
				LDI R0, CHARFORONE
				OUT
				BRnzp SHIFT 				


